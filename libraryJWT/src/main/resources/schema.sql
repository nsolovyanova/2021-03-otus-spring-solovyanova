DROP TABLE IF EXISTS AUTHORS;
CREATE TABLE AUTHORS(ID BIGINT IDENTITY PRIMARY KEY, NAME VARCHAR(255));
DROP TABLE IF EXISTS GENRES;
CREATE TABLE GENRES(ID BIGINT IDENTITY PRIMARY KEY, NAME VARCHAR(255));
DROP TABLE IF EXISTS BOOKS;
CREATE TABLE BOOKS(ID BIGINT IDENTITY PRIMARY KEY, NAME VARCHAR(255), AUTHOR_ID BIGINT, GENRE_ID BIGINT);
DROP TABLE IF EXISTS COMMENTS;
CREATE TABLE COMMENTS(ID BIGINT IDENTITY PRIMARY KEY, BOOK_ID BIGINT, TEXT_COMMENT VARCHAR(255));
DROP TABLE IF EXISTS USERS;
CREATE TABLE USERS(ID BIGINT IDENTITY PRIMARY KEY, LOGIN VARCHAR(255), PASSWORD VARCHAR(255));
DROP TABLE IF EXISTS USERSACCESS;
CREATE TABLE USERSACCESS(ID BIGINT IDENTITY PRIMARY KEY, USERID BIGINT, ACCESS VARCHAR(255));